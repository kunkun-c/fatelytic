// Translation functions
export const translateTerm = (category: keyof typeof ASTROLOGY_TRANSLATIONS, term: string): string => {
  const translations = ASTROLOGY_TRANSLATIONS[category];
  return translations[term as keyof typeof translations] || term;
};

export const translatePalaceName = (name: string): string => {
  return translateTerm('palaces', name);
};

export const translateStarName = (name: string): string => {
  // Check all star categories
  const categories = ['majorStars', 'minorStars', 'flowerStars', 'helperStars'] as const;
  for (const category of categories) {
    const translated = translateTerm(category, name);
    if (translated !== name) return translated;
  }
  return name;
};

export const translateBrightness = (brightness: string): string => {
  return translateTerm('brightness', brightness);
};

export const translateEarthlyBranch = (branch: string): string => {
  return translateTerm('earthlyBranches', branch);
};

export const translateHeavenlyStem = (stem: string): string => {
  return translateTerm('heavenlyStems', stem);
};

export const translateZodiac = (zodiac: string): string => {
  return translateTerm('zodiac', zodiac);
};

export const translateWesternZodiac = (sign: string): string => {
  return translateTerm('westernZodiac', sign);
};

export const translateCommon = (term: string): string => {
  // Try direct translation first
  const directTranslation = translateTerm('common', term);
  if (directTranslation !== term) return directTranslation;
  
  // Handle five elements class patterns like "火六局", "水二局", etc.
  const fiveElementsMatch = term.match(/^([火水木金土])([一二三四五六七八九十]+)局$/);
  if (fiveElementsMatch) {
    const element = fiveElementsMatch[1];
    const number = fiveElementsMatch[2];
    
    // Translate element
    const elementVi = translateFiveElement(element);
    
    // Map common Chinese numbers to Vietnamese readings
    const numberMap: Record<string, string> = {
      '一': 'Nhất',
      '二': 'Nhị',
      '三': 'Tam',
      '四': 'Tứ',
      '五': 'Ngũ',
      '六': 'Lục',
      '七': 'Thất',
      '八': 'Bát',
      '九': 'Cửu',
      '十': 'Thập'
    };
    
    const numberVi = numberMap[number] || number;
    return `${elementVi} ${numberVi} Cục`;
  }
  
  return term;
};

export const translateBrightnessExplanation = (code: string): string => {
  return translateTerm('brightnessExplanations', code);
};

export const translateFiveElement = (element: string): string => {
  return translateTerm('fiveElements', element);
};

export const getFiveElementColor = (element: string): string => {
  const colors = ASTROLOGY_TRANSLATIONS.fiveElementsColors;
  return colors[element as keyof typeof colors] || 'bg-gray-100 text-gray-800';
};

export const getStarColor = (starName: string): string => {
  const colors = ASTROLOGY_TRANSLATIONS.starColors;
  return colors[starName as keyof typeof colors] || 'text-gray-800';
};
